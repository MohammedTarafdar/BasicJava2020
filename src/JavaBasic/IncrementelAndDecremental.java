package JavaBasic;

public class IncrementelAndDecremental {

	public static void main(String[] args) {
		
		// Incremental Operator ++
		// Decremental Operator --
		
		
		int a = 1;
		a = a+1;
		
		System.out.println(a);
		System.out.println("");
		
		a ++;
		
		System.out.println(a);
		System.out.println("");
		
		a --;
		System.out.println(a);
		System.out.println("");
		
		int i = 1;
		int j = ++i;		// pre increment
		
		System.out.println(i);		//
		System.out.println(j);
		System.out.println("");

	}

}
