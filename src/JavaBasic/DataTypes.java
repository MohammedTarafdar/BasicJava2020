package JavaBasic;

public class DataTypes {
	
		// Single line comments
	
		/*
		* Multi-line comments
		*/

	public static void main(String[] args) {
		
		// Data types:
		
		//integer family:
		//1. byte:
			//size: 1 byte = 8 bits
			//range: -128 to 127
		
		byte b =10;
		byte b1 = -20;
		byte b2 = 123;
		
		System.out.println(b);
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("");
		
		//byte b2 = -123; Rule: Duplicate variable is not allowed in java.
		b2 = -123;
		
		System.out.println(b);
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("");
		

	}

}
